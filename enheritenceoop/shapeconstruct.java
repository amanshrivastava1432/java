package enheritenceoop;

public class shapeconstruct {
	String color;
	int borderWidth;
	public shapeconstruct(String color, int borderWidth) {
		super();
		this.color = color;
		this.borderWidth = borderWidth;
	}
	public String getColor() {
		return color;
	}
	public int getBorderWidth() {
		return borderWidth;
	}
}