package in.co.oop;

public class constructorarea {
	private int length;
	private int width;
	private int hieght;
	public constructorarea(int length, int width, int hieght) {
		super();
		this.length = length;
		this.width = width;
		this.hieght = hieght;
		
	}
	public int getLength() {
		return length;
	}
	public int getWidth() {
		return width;
	}
	public int getHieght() {
		return hieght;
	}
	
	

}
